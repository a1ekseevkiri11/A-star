function sigmoid(x) {
  return 1 / (1 + Math.exp( -x ));
}

function getMultiplicationMatrix(a, b) {
  let result = Array(a[0].length);
  for (let i = 0; i < a[0].length; i++) {
      let sum = 0;
      for (let j = 0; j < a.length; j++) {
          sum += a[j][i] * b[j];
      }
      result[i] = sigmoid(sum);
  }
  return result;
}

